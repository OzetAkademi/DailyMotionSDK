/// <summary>
/// User limits information
/// </summary>
public class UserLimits
{
    /// <summary>
    /// Gets or sets the duration of the video.
    /// </summary>
    /// <value>The duration of the video.</value>
    public long VideoDuration { get; set; }
    /// <summary>
    /// Gets or sets the size of the video.
    /// </summary>
    /// <value>The size of the video.</value>
    public long VideoSize { get; set; }
}
